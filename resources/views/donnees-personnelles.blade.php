					<h1 id="item-1-0">POLITIQUE DE PROTECTION DES DONNÉES</h1>
					<p>Le RGPD impose une information complète et précise. Les modalités de fourniture et de présentation de cette information doivent être adaptées au contexte.</p>
					<p>La transparence permet aux personnes concernées :</p>
					<ul>
					<li>de connaître la raison de la collecte des différentes données les concernant;</li>
					<li>de comprendre le traitement qui sera fait de leurs données;</li>
					<li>d'assurer la maîtrise de leurs données, en facilitant l'exercice de leurs droits.</li>
					<li>Pour les responsables de traitement, elle contribue à un traitement loyal des données et permet d'instaurer une relation de confiance avec les personnes concernées.</li>
					</ul>

					<h2 id="item-2-1">INTRODUCTION</h2>
					<p>Code Puzzle est amené à collecter et à traiter des informations dont certaines sont qualifiées de "données personnelles". Code Puzzle limite la collecte des données personnelles au strict nécessaire.</p>

					<h2 id="item-2-2">DONNÉES TRAITÉES</h2>
					<p>Les données personnelles collectées lors de la création d'un compte sont : prénom, nom, matière, établissement, adresse électronique et mot de passe. Aucun mot de passe n'est sauvegardé en clair. Tous les mots de passe sont chiffrés dans la base de données.</p>

					<h2 id="item-2-3">BASE LÉGALE</h2>
					<p>Les données personnelles ne sont collectées qu'après consentement obligatoire de l'utilisateur. Ce consentement est valablement recueilli lors de la création du compte (information et case à cocher). Le consentement est libre, clair et sans équivoque.</p>

					<h2 id="item-2-4">UTILISATION</h2>
					<p>Les données que vous nous transmettez sont utilisées :</p>
					<ul>
					<li>pour pouvoir accéder à la console;</li>
					<li>dans le but de contacter la personne si nécessaire.</li>
					</ul>
					Ces données ne seront jamais cédées à un tiers, ni utilisées à d'autres fins que celles détaillées ci-dessus.

					<h2 id="item-2-5">HÉBERGEMENT DES DONNÉES À CARACTÈRE PERSONNEL</h2>
					<p>Les données à caractère personnel collectées et traitées sont hébergées en France sur le serveur de Code Puzzle. Les données à caractère personnel collectées par Code Puzzle ne sont transmises à aucun tier. Elles sont conservées sur le serveur de Code Puzzle jusqu'à expiration (voir "Durée de conservation").</p>

					<h2 id="item-2-6">DURÉE DE CONSERVATION</h2>
					<p>Les données des comptes sont conservées durant une durée maximale de 2 ans. Elles peuvent être supprimées à tout moment par l'utilisateur en cliquant, dans la console, sur "supprimer le compte".</p>

					<h2 id="item-2-7">FINALITÉS DES TRAITEMENTS</h2>
					<p>La collecte et le traitement des données répondent aux finalités suivantes : accéder à la console et communication.

					<h2 id="item-2-8">COOKIES</h2>
					Ce site n'utilise que deux 'cookies' nécessaires au fonctionnement du site. Ils permettent de gérer la navigation et d'éviter les attaques extérieures par injection de code malicieux via un formulaire. Ces 'cookies' ne contiennent aucune donnée personnelle et ils ont une durée de vie de trois heures.</p>

